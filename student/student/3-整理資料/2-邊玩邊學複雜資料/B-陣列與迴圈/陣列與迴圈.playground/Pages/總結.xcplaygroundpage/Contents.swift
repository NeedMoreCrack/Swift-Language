## 總結

 你可以使用陣列來容納一系列項目。陣列有兩種主要特色：

- 陣列中的項目全部都是同一個類型。
- 陣列中的項目會以特定順序排列。
 
 因為有了這兩項特色，你可以使用索引從陣列中的特定點存取項目，且程式也必定會傳回已知類型的值。
 
 以下是你已學會的陣列相關知識：

- 陣列的第一個索引是零，不是一。
- 使用索引存取陣列可能會有危險。如果使用的索引位於陣列界限外，你的程式將會異常終止。
- 你可以使用 `count` 屬性找出陣列中的項目數目。
- 你可以使用 `for…in` 迴圈來依序安全存取陣列中的每一個項目，而無須知道陣列中包含的項目數量。
- 可變更的陣列可讓你新增、移除與取代項目。
- 你可以透過將一個陣列附加到另一個陣列的方式結合兩個資料來源，然後將陣列作為單一來源來處理。
 
 請利用練習來對所學內容進行實作。你會發現透過撰寫程式碼篩選資料來回答問題，或找出資料集合中的模式，是多麼有用。

