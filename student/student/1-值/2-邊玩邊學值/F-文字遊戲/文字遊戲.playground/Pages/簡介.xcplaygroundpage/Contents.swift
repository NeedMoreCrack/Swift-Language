## 文字遊戲

 在此 Playground 中，你將使用對值、常數和字串的了解來打造有趣的文字遊戲，與同學和朋友一起玩。
 
 遊戲方法如下：寫一篇小故事、笑話、名言佳句或其他形式的短句 (或直接使用現有的敘事短文也可以)。從文字中選擇一些單詞或詞組，讓其他參與遊戲的人可以用自己的詞組替換。參與遊戲的人先不要看故事，直接從每個單詞選出「值」，然後將故事中的單詞換成其他人選擇的單詞，接著列印出來。
 
 為了讓最後的文字有意義，你必須注意每個單詞或詞組所扮演的角色。假設你選擇了以下文字：

 _One accurate measurement is worth a thousand expert opinions._ (Grace Hopper)

 選擇一些關鍵詞，然後替換詞性或換成其他詞語，將會產生：

 _One **<adjective>** **<noun>** is worth **<number>** **<adjective>** **<plural noun>**._

 參與遊戲的人拿到單詞列表後，便隨機選擇值：

 **adjective (形容詞)**：furry\
 **noun (名詞)**：spoon\
 **number (數字)**：17\
 **adjective (形容詞)**：amazing\
 **plural noun (複數名詞)**：umbrellas

 然後列印：

 One furry spoon is worth 17 amazing umbrellas.(Grace Hopper)

