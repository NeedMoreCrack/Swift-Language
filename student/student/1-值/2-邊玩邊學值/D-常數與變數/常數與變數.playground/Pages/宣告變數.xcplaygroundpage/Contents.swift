/*:
## 宣告變數

 宣告變數和宣告常數幾乎完全一樣。只是不使用關鍵字 `let`，而是使用關鍵字 `var`：
 */
// Declaring a constant
let placeOfBirth = "New Jersey"

// Declaring a variable
var currentLocation = "New Jersey"

//:  在你宣告變數後，可以指派給它一個新的值：
// Assigning a new value to a variable
currentLocation = "California"

//:  - callout(實驗：): 在下方新增一行程式碼，將新值指定為 `currentLocation`：
// Assign a new value

/*:
 在大多數情況下，常數和變數是相同：
- 都使用相同的名稱規則
- 名稱都與所指定的值相關聯
- 都有特定類型

 差異在於：
- 首次指定常數的值之後，即「無法」再變更
- 首次指定變數的值之後，「能夠」變更

