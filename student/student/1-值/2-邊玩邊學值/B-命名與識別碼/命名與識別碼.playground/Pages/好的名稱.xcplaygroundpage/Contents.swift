 - callout(為何不？：): 為什麼不使用表情符號作為名稱？其中一個原因是難以一眼辨別出表情符號，另一個原因是並非每個用例都有表情符號可用。如果有人帶了一隻寵物狼蛛到寵物展怎麼辦？它並沒有相對應的表情符號可用。使用字詞會更有彈性且絕對更可靠。

