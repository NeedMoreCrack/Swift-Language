## 識別碼
在 Swift 中，名稱正式稱為「識別碼」，你將會在錯誤訊息中看見使用「識別碼」一詞。但程式設計師通常會使用較通俗的詞彙稱呼，即為「名稱」。在本課程中，你將會熟悉這兩個詞彙。

常數的名稱以小寫字母開頭。如下方範例所示，常數會包含一些字，全部連在一起。(如果單字之間有空格，Swift 會將每個字視為不同的識別碼。)

若要讓含有多個字的識別碼更容易閱讀，除了第一個字外，之後每個字的第一個字母都應大寫。這種編寫名稱的方式稱為「駝峰式大小寫」，因為名稱中間的大寫字母看起來有點像是駱駝的駝峰。🐫
 */
