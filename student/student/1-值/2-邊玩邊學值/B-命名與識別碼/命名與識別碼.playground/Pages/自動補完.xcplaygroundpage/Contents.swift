## 輸入名稱和自動補完
Xcode 會記錄你定義的所有名稱。因此當你開始輸入時，會看到建議的名稱選單 (Xcode 記錄的名稱)。這稱為「自動補完」。

自動補完會協助你加快編寫程式碼的速度，且減少輸入錯誤的次數。輸入越多，自動補完選單的範圍會縮小，讓你更容易找到要找的項目。

每當自動補完選單出現時，你就可以：
- 繼續輸入來縮小列表範圍。
- 使用向上鍵和向下鍵來上下移動列表。
- 點擊 Return 鍵讓 Xcode 填入選取的名稱。

![自動補完彈出式選單](autocomplete.png)
在列表中，你將會看到未曾定義的名稱。你可以忽略這些名稱。

