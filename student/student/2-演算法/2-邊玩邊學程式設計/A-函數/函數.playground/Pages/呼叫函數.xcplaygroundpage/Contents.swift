## 呼叫函數

 當你使用 `print()` 將 “Hello, world!” 列印到主控台時，早已善用過抽象概念和函數。在 Swift 中，`print()` 是一個函數。使用它時，就是在「呼叫」函數：
 */
print("Hello, world!")
print(360)
/*:
如同你穿衣服時有許多步驟，當你呼叫 `print()` 函數時也會發生很多情況，包含：
- 將你提供的任何內容 (包括數字) 轉變成字串。
- 加入新的一行字元，每次呼叫 `print()` 後都會多出新的一行。
- 讓該字串顯示在主控台中。

在此情況下，你呼叫的是其他人建立的函數。你不需要知道 `print()` 的所有運作細節來呼叫它。

 可重複使用是函數為何如此強大的主要原因。這些函數提供了一種方式，將詳細步驟組合成可重複使用的定義。

