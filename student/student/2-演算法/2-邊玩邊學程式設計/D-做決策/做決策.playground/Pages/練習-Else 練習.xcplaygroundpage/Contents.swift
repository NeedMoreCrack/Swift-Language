/*:
## 練習：Else 練習

 回想一下，`else` 語句程式碼是在 `if` 語句的結果不為 true 時才執行。它們可以減少所需的程式碼量，讓程式的流程更加清楚。
 */
let secretWord = "banana"
let guess = "apple"

if secretWord == guess {
    print("Your guess is right.")
}
if secretWord != guess {
    print("Your guess is wrong.")
}

