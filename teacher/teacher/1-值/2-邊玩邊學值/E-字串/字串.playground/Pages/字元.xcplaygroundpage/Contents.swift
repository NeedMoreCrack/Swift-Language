## 字元
當然，文字的內容可以不光是字母而已。也可以包括標點符號、數字、符號，甚至是空格，這些都稱為「字元」。所有的「字串」皆由「字元」組成。

字母是字元：
## Aa Bb Cc Dd Ee
標點符號也是：
## ! ? . , ; :
數字：
## 1 2 3 4 5 6 7 8 9 0
符號：
## # @ $ & © π * + - /
甚至包含隱形字元，如空格和定位字元：「(你看不見它們，但它們確實存在。)」
## `                         `

根據正確定義，`String` 是一組「字元序列」，按照特定順序排列的一組東西，就像上一頁中提到的串珠一樣。

現在你已了解這些組成元素，可以開始建構字串了。\
