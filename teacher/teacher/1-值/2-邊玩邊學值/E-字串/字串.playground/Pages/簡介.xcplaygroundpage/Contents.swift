## 字串
不論是即時訊息、新聞閱讀器、社群媒體、地圖，幾乎所有的 app 都可以看到文字。甚至相機 app 也可能使用文字來標示按鈕和控制項。

在大多數程式語言 (包括 Swift) 中，文字值稱為「字串」。

### 為什麼是「字串」？
想像一下串珠項鍊。每顆珠子可能各有不同，但是都按照特定順序串接在一起。

![一串珠子](StringOfBeads.png)
單一字母 (例如「H」) 通常本身不具意義。但是一串字母可以建立一個單字，因此程式設計師將文字稱為「字串」。字串可以是任何你想像得到的文字。

![字元串](StringOfCharacters.png)

