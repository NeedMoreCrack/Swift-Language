## 總結

 你已學到了此 Playground 中一些非常重要的概念：

- 使用 `let` 宣告的值是常數，一旦指定值之後就無法變更。這些值稱為「不可變更的」。
- 使用 `var` 宣告的值是變數，隨著時間可指定新的值。這些值稱為「可變更的」。
- 可變更的值可以用於自身的賦值語句的一部分：`score = score + 10`。
- 複合賦值運算子允許更新可變更的值：`score += 10`。
- 將常數和變數用在正確的位置可讓你的程式碼更加安全、更容易理解。
 
