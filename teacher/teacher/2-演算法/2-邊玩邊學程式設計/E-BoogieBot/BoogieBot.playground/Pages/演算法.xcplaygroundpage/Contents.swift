## 演算法

 在這堂課中，透過合併舞步為函數，你已經創造了不同的 BoogieBot 舞步組合。電腦科學家會說這堂課是教你學會建立「演算法」。
 
建立演算法就是定義一系列要執行的步驟。當然，這些步驟不一定是舞步；任何一套獨立的逐步執行的操作都是演算法。
 
定義演算法在程式設計中占很大的比重。有些演算法屬於複雜的數學演算法，例如壓縮影片，以便可以在網路上有效率地傳送影片。有些演算法根本不涉及數學，例如，檢查 app 使用者是否收到新資訊並在收到資訊時顯示通知。當然，有些演算法是讓軟體機器人進行舞蹈。
 
