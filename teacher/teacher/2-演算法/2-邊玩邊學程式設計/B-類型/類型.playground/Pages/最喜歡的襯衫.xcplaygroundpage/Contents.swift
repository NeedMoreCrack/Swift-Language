## 最喜歡的襯衫

 如果你花點時間想一件你最喜歡的襯衫，不太可能是香蕉。這沒有任何意義。香蕉不是襯衫。
 
 另一方面，如果你想出一種最喜歡的水果，無論是香蕉、蘋果還是什麼，它一定得是某種水果。
 
 大家為某件東西命名時 (例如，「最喜歡的水果」)，該名稱通常會與特定類型的事物連結起來。
 
 「最喜歡的水果」這一概念只有在與某種水果連結起來時才有意義。與「活動扳手」或「天文學」連結起來則毫無意義。你的大腦能夠精準確定名稱是否與正確的事物類型相符合。
 
 同樣地，Swift 會追蹤與常數和變數相關聯的數值類型，並確保你不會誤將原本應該是襯衫的值設定為香蕉。
 
