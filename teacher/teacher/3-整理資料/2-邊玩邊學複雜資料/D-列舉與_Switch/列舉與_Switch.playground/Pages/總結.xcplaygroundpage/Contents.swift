## 總結

 列舉是用於你要代表一群相關數值中的其中一個數值時。可能的數值稱為用例。
 
 建立列舉時，你就是在建立新的類型。該類型的實例只能擁有對應其中一個指定用例的數值。
 
 使用列舉能讓你的程式碼更容易讀寫，因為可能的數值以及其代表的意義都相當明確。
 
 你可以使用 `==` 比較列舉值，或使用 switch 語句來測試所有可能的值。
 
 Switch 語句就像 if 語句，是你程式碼中另一種決策方式。Switch 語句非常適合用於列舉，但也可用於轉換任何類型的數值。
 
 由於 switch 語句必須窮舉，因此你須處理每個可能的數值。若要處理未經指定的任何數值，你可使用預設用例。 
 
 和結構一樣，你可以對列舉新增運算屬性和方法。
 
