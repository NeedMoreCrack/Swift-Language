## 像素畫

 回到個人計算機的舊時代，那時點陣式顯示器是嶄新的裝置，與今時相比，像素在當時是相當寶貴的產品。例如，在 1980 年代，Apple II 的顯示器是 40x40 像素，用於顯示解析度非常低的圖形。顏色也沒有那麼豐富。當時有許多顯示器的像素只能顯示與不顯示，好一點的只能顯示一些顏色。Apple II 在低解析度圖形模式中，提供使用者 16 種顏色的選擇。
 
 程式設計師當時要費很多功夫一個一個處理像素，手工處理螢幕上所有的圖形。
 
 在此 Playground 中，你將一次一個像素地建立低解析度圖形。身為 app 開發者，你不可能會這麼做，但這是一個很有趣的方法，能探索圖形的一些基本概念，讓你以過去開發者的角度思考。
 
